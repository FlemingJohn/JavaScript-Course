// variables - used to store values 
// types - var, let and const 
// var and cont - immutable
// let - mutable 

// defining the variables
var x = 10;
let y = 5;
const z = 15;

// printing 
console.log(x);
console.log(y);
console.log(z);


// creating the object in const 
const person = {
    name: "Fleming",
    age: 21
}

// changing the values of the object 
person.name = "John";
console.log(person);